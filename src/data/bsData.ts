// Nepali numerals
export const NP_DIGITS = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'];

export function toNepaliNumeral(num: number): string {
  return num.toString().split('').map(d => NP_DIGITS[parseInt(d)]).join('');
}

export function getTotalDaysInBsYear(bsYear: number): number {
  const months = BS_CALENDAR_DATA[bsYear];
  if (!months) return 0;
  return months.reduce((sum, days) => sum + days, 0);
}

export function getDaysInBsMonth(bsYear: number, bsMonth: number): number {
  const months = BS_CALENDAR_DATA[bsYear];
  if (!months || bsMonth < 1 || bsMonth > 12) return 0;
  return months[bsMonth - 1];
}

